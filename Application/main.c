    buttons_leds_init(&erase_bonds, bsp_event_handler);
    power_management_init();
    ble_stack_init();
    scheduler_init();
    gap_params_init();
    gatt_init();
    advertising_init();
	
				qwr_init(&m_qwr);
				dis_init();
				bas_init();
				hids_init();
